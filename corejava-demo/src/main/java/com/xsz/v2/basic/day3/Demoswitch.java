package com.xsz.v2.basic.day3;

public class Demoswitch {
    public static void main(String[] args) {
        int a=4;
        switch (a){
            case 1:
                System.out.println("你好");
            break;

            case 2:
                System.out.println("Hello");
                break;
            case 3:
                System.out.println("张飞");
                break;
            case 4:
                System.out.println("赵云");
                break;
        }
    }
}
